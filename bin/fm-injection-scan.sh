#!/usr/bin/env bash
# Firstmate-internal review-stage scanner for prompt-injection and honeypot
# payloads planted in a crewmate's diff. Runs BEFORE firstmate relays a PR to the
# captain or pushes upstream (AGENTS.md section 7, alongside fm-review-diff.sh).
#
# IMPORTANT LIMITATION: this is a DETERMINISTIC SYMPTOM-CATCHER, not a semantic
# injector-detector. It catches the obvious / planted-payload class of attacks
# (honeypot notice files, self-incriminating reveal text, hidden HTML-comment or
# zero-width instructions, base64 blobs, "ignore previous instructions" lines).
# A truly subtle semantic injection still requires the captain's eyeball plus the
# ordinary no-mistakes review. Any finding = STOP-AND-INVESTIGATE: never auto-ship
# a flagged diff, and never auto-delete a flagged file (it is evidence).
#
# Scans ADDED lines / NEW files only - it never flags pre-existing upstream
# content, so a baseline that already contains an odd string is not a finding.
#
# Usage:
#   fm-injection-scan.sh <task-id>           # resolve the diff from task meta
#   fm-injection-scan.sh --diff <file|->     # scan a unified diff file ('-' = stdin)
#   fm-injection-scan.sh                     # read a unified diff from stdin
#       (so this composes: bin/fm-review-diff.sh <id> | bin/fm-injection-scan.sh)
#   fm-injection-scan.sh --quiet ...         # suppress output; exit code only
#   fm-injection-scan.sh --help | -h
# Exit codes: 0 = CLEAN, 1 = FINDINGS present, 2 = usage/runtime error.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
FM_ROOT="${FM_ROOT_OVERRIDE:-$(cd "$SCRIPT_DIR/.." && pwd)}"
FM_HOME="${FM_HOME:-${FM_ROOT_OVERRIDE:-$FM_ROOT}}"
STATE="${FM_STATE_OVERRIDE:-$FM_HOME/state}"

# ---------------------------------------------------------------------------
# Pattern sets - the threat evolves, so keep these editable and conservative.
# Every match is a REVIEW flag, never an auto-action: a false positive costs a
# human glance, a false negative ships a honeypot.
# ---------------------------------------------------------------------------

# New-file basenames matched case-insensitively as shell globs that look like
# notices / confessions / markers planted by a honeypot. Lowercase globs here.
SUSPICIOUS_FILE_PATTERNS=('ai_*' '*notice*' '*confess*' '*marker*' 'agent_*' '*banner*' '*advisory*')

# Self-incriminating / AI-reveal phrases in ADDED content (lowercase substrings).
REVEAL_PHRASES=(
  "i am an ai"
  "i am a language model"
  "i am a large language model"
  "i am a sad"
  "i am a dumb"
  "sad, dumb"
  "as an ai"
  "as a language model"
  "generated by ai"
  "generated by an ai"
  "ai-generated"
  "i am an automated"
  "i am a bot"
  "this pr was generated"
  "this pr is automated"
  "automated pull request"
  "i have no real skills"
)

# Instruction-shaped injection phrases in ADDED content (lowercase substrings).
INJECTION_PHRASES=(
  "ignore previous"
  "ignore all previous"
  "disregard previous"
  "disregard the above"
  "disregard all previous"
  "system prompt"
  "new instructions:"
  "you are an ai"
  "you are a language model"
  "you are now a"
  "forget your instructions"
  "override your instructions"
)

# Minimum run length of a base64-alphabet blob to flag as suspicious.
BASE64_MIN_LEN=40

# Heuristic "looks like code" markers - if a new root file's short added content
# matches any of these, it is left alone (not a honeypot marker). Stored in a
# variable because the bracket chars confuse bash's [[ =~ ]] literal-form parser.
CODE_LIKE_RE='[={};()<>]|function|def |import |class |return|const |let |var |->|=>|:='

# Zero-width / invisible unicode code points encoded as UTF-8 byte strings,
# matched as literal substrings. U+200B U+200C U+200D U+2060 U+FEFF.
ZW_CHARS=(
  "$(printf '\xe2\x80\x8b')"   # U+200B zero-width space
  "$(printf '\xe2\x80\x8c')"   # U+200C zero-width non-joiner
  "$(printf '\xe2\x80\x8d')"   # U+200D zero-width joiner
  "$(printf '\xe2\x81\xa0')"   # U+2060 word joiner
  "$(printf '\xef\xbb\xbf')"   # U+FEFF zero-width no-break space / BOM
)

# Build a single ERE alternation (lowercase) from a phrase array for =~ matching.
join_alt() {
  local IFS='|'
  printf '%s' "$*"
}
REVEAL_RE=$(join_alt "${REVEAL_PHRASES[@]}")
INJECTION_RE=$(join_alt "${INJECTION_PHRASES[@]}")
# Bash =~ interval needs the base64 length baked into the regex.
BASE64_RE="([A-Za-z0-9+/]{${BASE64_MIN_LEN},})"

usage() {
  cat <<'EOF' >&2
usage: fm-injection-scan.sh <task-id> [--quiet]
       fm-injection-scan.sh --diff <file|-> [--quiet]
       fm-injection-scan.sh [--quiet]   # unified diff on stdin

Deterministic symptom-catcher for prompt-injection / honeypot payloads in a
crewmate diff. Scans ADDED lines / NEW files only.

Exit codes: 0 CLEAN, 1 FINDINGS, 2 usage/runtime error.
--quiet: suppress output; signal via exit code only.

LIMITATION: this is NOT a semantic injector-detector. It catches the obvious /
planted-payload class (notice files, AI-reveal text, hidden HTML-comment or
zero-width instructions, base64 blobs, "ignore previous" lines). A subtle
semantic injection still needs human review + the normal no-mistakes gate.
Any finding = stop-and-investigate; never auto-ship, never auto-delete.
EOF
}

# ---------------------------------------------------------------------------
# Diff source resolution for <task-id> mode (mirrors fm-review-diff.sh's base
# resolution so this tool is self-contained - it does not depend on the exact
# stdout format of fm-review-diff.sh).
# ---------------------------------------------------------------------------
default_branch_of() {
  local proj=$1 ref b
  ref=$(git -C "$proj" symbolic-ref --quiet --short refs/remotes/origin/HEAD 2>/dev/null || true)
  if [ -n "$ref" ]; then
    printf '%s\n' "${ref#origin/}"
    return 0
  fi
  for b in main master; do
    if git -C "$proj" show-ref --verify --quiet "refs/heads/$b"; then
      printf '%s\n' "$b"
      return 0
    fi
  done
  return 1
}

resolve_task_diff() {
  local id=$1 meta wt proj default branch base
  meta="$STATE/$id.meta"
  [ -f "$meta" ] || { echo "error: no meta for task $id at $meta" >&2; return 2; }
  wt=$(grep '^worktree=' "$meta" | cut -d= -f2-)
  proj=$(grep '^project=' "$meta" | cut -d= -f2-)
  [ -n "$wt" ] || { echo "error: meta for $id is missing worktree=" >&2; return 2; }
  [ -n "$proj" ] || { echo "error: meta for $id is missing project=" >&2; return 2; }
  [ -d "$wt" ] || { echo "error: worktree for $id is missing: $wt" >&2; return 2; }
  [ -d "$proj" ] || { echo "error: project for $id is missing: $proj" >&2; return 2; }
  default=$(default_branch_of "$proj") || { echo "error: cannot determine default branch for $proj" >&2; return 2; }
  branch="fm/$id"
  if ! git -C "$wt" rev-parse --verify --quiet "refs/heads/$branch" >/dev/null; then
    branch=$(git -C "$wt" symbolic-ref --quiet --short HEAD 2>/dev/null || true)
    [ -n "$branch" ] || { echo "error: cannot resolve branch for $id in $wt" >&2; return 2; }
  fi
  if git -C "$proj" remote get-url origin >/dev/null 2>&1; then
    git -C "$wt" fetch origin "+refs/heads/$default:refs/remotes/origin/$default" --quiet 2>/dev/null || true
    base="origin/$default"
  else
    base="$default"
  fi
  git -C "$wt" rev-parse --verify --quiet "$base^{commit}" >/dev/null || { echo "error: base $base does not exist in $wt" >&2; return 2; }
  git -C "$wt" rev-parse --verify --quiet "$branch^{commit}" >/dev/null || { echo "error: branch $branch does not resolve in $wt" >&2; return 2; }
  git -C "$wt" diff "$base...$branch" --
}

# ---------------------------------------------------------------------------
# The scanner. Reads a unified diff on stdin; appends human-readable finding
# strings to the global FINDINGS array.
# ---------------------------------------------------------------------------
declare -A FILE_IS_NEW=() FILE_ADDED_COUNT=() FILE_JOINED=()
FINDINGS=()

emit() {
  FINDINGS+=("FINDING: $1 - $2")
}

# New-file basename check. Args: file is_new
check_filename() {
  local f=$1 is_new=$2 base lcb pat
  [ "$is_new" = 1 ] || return 0
  base=${f##*/}
  lcb=${base,,}
  for pat in "${SUSPICIOUS_FILE_PATTERNS[@]}"; do
    # shellcheck disable=SC2254  # $pat is intentionally an unquoted glob here
    case "$lcb" in
      $pat) emit "$f" "new file name matches suspicious pattern '$pat' (review; do not auto-delete)"; return 0 ;;
    esac
  done
  return 0
}

# Added-content check. Args: file lineno is_new content
check_added() {
  local f=$1 ln=$2 is_new=$3 content=$4 lc m c
  lc=${content,,}
  if [[ $lc =~ $REVEAL_RE ]]; then
    emit "$f:$ln" "self-incriminating / AI-reveal text"
  fi
  if [[ $lc =~ $INJECTION_RE ]]; then
    emit "$f:$ln" "instruction-shaped injection text"
  fi
  if [[ $content == *'<!--'* ]]; then
    emit "$f:$ln" "hidden HTML comment (possible embedded instruction)"
  fi
  if [[ $content =~ $BASE64_RE ]]; then
    m=${BASH_REMATCH[1]}
    # A 40/64-char all-hex run is almost certainly a git SHA, not a payload.
    if [[ ! $m =~ ^[0-9a-fA-F]+$ ]]; then
      emit "$f:$ln" "long base64-looking blob (${#m} chars)"
    fi
  fi
  for c in "${ZW_CHARS[@]}"; do
    if [[ $content == *"$c"* ]]; then
      emit "$f:$ln" "zero-width / invisible unicode"
      break
    fi
  done
  return 0
}

# Backstop for the "echo of an external instruction" class: a NEW file at the
# repo root whose added content is short and does not look like code. The main
# honeypot is already caught by the filename/reveal checks; this catches a
# marker whose text is otherwise bland. Args: file
evaluate_honeypot_echo() {
  local f=$1 is_new n joined
  [ -n "$f" ] || return 0
  is_new=${FILE_IS_NEW[$f]:-0}
  [ "$is_new" = 1 ] || return 0
  case "$f" in
    */*) return 0 ;;   # only root-level banner files
  esac
  n=${FILE_ADDED_COUNT[$f]:-0}
  [ "$n" -eq 1 ] || return 0
  joined=${FILE_JOINED[$f]:-}
  joined=${joined,,}
  [ "${#joined}" -lt 240 ] || return 0
  # If it looks like code (assignment, braces, parens, arrows, keywords), leave it.
  if [[ $joined =~ $CODE_LIKE_RE ]]; then
    return 0
  fi
  emit "$f" "new root file with short non-code content (possible honeypot marker)"
  return 0
}

# Extract the new-file start line number from a hunk header. Args: "@@ -a,b +c,d @@"
parse_new_start() {
  local h=$1 seg
  case "$h" in
    *' +'*)
      seg=${h#*' +'}      # "c,d @@ ctx"
      seg=${seg%% *}      # "c,d"
      seg=${seg%%,*}      # "c"
      case "$seg" in
        ''|*[!0-9]*) printf '0\n' ;;
        *) printf '%s\n' "$seg" ;;
      esac
      ;;
    *) printf '0\n' ;;
  esac
}

# Reads a unified diff on stdin.
run_scan() {
  local line content cur_file="" cur_is_new=0 in_hunk=0 new_lineno=0 this_ln
  while IFS= read -r line || [ -n "${line:-}" ]; do
    case "$line" in
      'diff --git '*)
        evaluate_honeypot_echo "$cur_file"
        cur_file=""; cur_is_new=0; in_hunk=0; new_lineno=0
        ;;
      'new file mode '*)
        cur_is_new=1
        ;;
      '--- /dev/null')
        cur_is_new=1
        ;;
      '+++ b/'*)
        cur_file=${line#'+++ b/'}
        FILE_IS_NEW["$cur_file"]=$cur_is_new
        FILE_ADDED_COUNT["$cur_file"]=0
        FILE_JOINED["$cur_file"]=""
        check_filename "$cur_file" "$cur_is_new"
        ;;
      '+++ /dev/null')
        cur_file="/dev/null"
        ;;
      '@@'*)
        new_lineno=$(parse_new_start "$line")
        in_hunk=1
        ;;
      '+'*)
        content=${line#'+'}
        if [ "$in_hunk" = 1 ]; then
          this_ln=$new_lineno
          new_lineno=$((new_lineno + 1))
        else
          this_ln=0
        fi
        if [ -n "$cur_file" ] && [ "$cur_file" != "/dev/null" ]; then
          check_added "$cur_file" "$this_ln" "$cur_is_new" "$content"
          FILE_ADDED_COUNT["$cur_file"]=$((${FILE_ADDED_COUNT["$cur_file"]:-0} + 1))
          FILE_JOINED["$cur_file"]+="${FILE_JOINED["$cur_file"]:+ }$content"
        fi
        ;;
      '-'*)
        : # deletion; does not advance the new-file line number
        ;;
      ' '*)
        [ "$in_hunk" = 1 ] && new_lineno=$((new_lineno + 1))
        ;;
      *)
        : # index, rename, mode, "\ No newline" etc. - ignored
        ;;
    esac
  done
  evaluate_honeypot_echo "$cur_file"
}

# ---------------------------------------------------------------------------
# Argument parsing
# ---------------------------------------------------------------------------
QUIET=0
DIFF_FILE=
ID=
while [ $# -gt 0 ]; do
  case "$1" in
    --help|-h) usage; exit 0 ;;
    --quiet) QUIET=1; shift ;;
    --diff) DIFF_FILE=${2:-}; shift 2 || { usage; exit 2; } ;;
    --diff=*) DIFF_FILE=${1#--diff=}; shift ;;
    -) DIFF_FILE=-; shift ;;
    -*) echo "error: unknown option: $1" >&2; usage; exit 2 ;;
    *) ID=$1; shift ;;
  esac
done

if [ -n "$DIFF_FILE" ] && [ -n "$ID" ]; then
  echo "error: pass either a <task-id> or --diff, not both" >&2
  exit 2
fi

TMP_DIFF=
# shellcheck disable=SC2329  # cleanup is invoked via the EXIT trap below
cleanup() {
  # shellcheck disable=SC2317  # invoked via the EXIT trap below
  [ -z "$TMP_DIFF" ] || rm -f "$TMP_DIFF"
}
trap cleanup EXIT

# Select the diff source and run the scan in THIS shell so FINDINGS propagates.
if [ -n "$DIFF_FILE" ]; then
  if [ "$DIFF_FILE" = "-" ]; then
    run_scan
  else
    [ -r "$DIFF_FILE" ] || { echo "error: cannot read diff file: $DIFF_FILE" >&2; exit 2; }
    run_scan < "$DIFF_FILE"
  fi
elif [ -n "$ID" ]; then
  TMP_DIFF=$(mktemp "${TMPDIR:-/tmp}/fm-injection-scan.XXXXXX")
  if ! resolve_task_diff "$ID" > "$TMP_DIFF"; then
    rm -f "$TMP_DIFF"
    exit 2
  fi
  run_scan < "$TMP_DIFF"
else
  run_scan
fi

# Report.
if [ "${#FINDINGS[@]}" -eq 0 ]; then
  [ "$QUIET" = 1 ] || echo "CLEAN"
  exit 0
fi

if [ "$QUIET" = 0 ]; then
  printf '%s\n' "${FINDINGS[@]}"
fi
exit 1
